'''range(start,end,step_size) --> end is till end-1'''

for i in range(8): # generates and displays number from 0 to 7
   print(i,'\n')

for i in range(1,8): # generates and displays number from 1 to 7
   print(i,'\n')

for i in range(2,8,2): # generates and displays number from 2 to 7 with step 2
   print(i)

